  defp null_pointer do
    <<0x00>>
  end

  defp add_padding(packet) do
    packet <> padding_for(packet)
  end

  defp padding_for(packet) do
    for _ <- 1..(188 - byte_size(packet)) do
      <<255>>
    end
    |> Enum.join()
  end
